CREATE TABLE DUVIDA(
	 ID NVARCHAR(40) NOT NULL PRIMARY KEY,
	 PERGUNTA NVARCHAR(4000) NOT NULL,
	 PONTOS INT NOT NULL,
	 DATAHORA DATETIME NOT NULL,
	 USUARIOID NVARCHAR(40) NOT NULL FOREIGN KEY REFERENCES USUARIO (ID),
	 MATERIAID NVARCHAR(40) NOT NULL FOREIGN KEY REFERENCES MATERIA (ID)
) 
GO

CREATE TABLE RESPOSTA(
	 ID NVARCHAR(40) NOT NULL PRIMARY KEY,
	 DESCRICAO VARCHAR(8000) NOT NULL,
	 MELHORRESPOSTA BIT NOT NULL DEFAULT 0,
	 DATAHORA DATETIME NOT NULL,
	 USUARIOID NVARCHAR(40) NOT NULL FOREIGN KEY REFERENCES USUARIO (ID),
	 DUVIDAID NVARCHAR(40) NOT NULL FOREIGN KEY REFERENCES DUVIDA (ID)
) 
GO

CREATE TABLE CLASSIFICACAORESPOSTA(
	 ID NVARCHAR(40) NOT NULL PRIMARY KEY,
	 CLASSIFICACAO INT NOT NULL,
	 USUARIOID NVARCHAR(40) NOT NULL FOREIGN KEY REFERENCES USUARIO (ID),
	 RESPOSTAID NVARCHAR(40) NOT NULL FOREIGN KEY REFERENCES RESPOSTA (ID)
) 
GO